//checks whether string is empty or not

void main() {
  String str1 = " Hey there!";
  String str2 = " ";
  print("String-1 Empty? -->${str1.isEmpty}"); //checks if str1 is empty.
  print(
      "String-1 Not Empty? -->${str1.isNotEmpty} \n"); //checks if str1 is not empty.
  print("String-2 Empty? -->${str2.isEmpty}"); //checks if str2 is empty.
  print(
      "String-2 Not  Empty? -->${str2.isNotEmpty}"); //checks if str2 is not empty.
}
